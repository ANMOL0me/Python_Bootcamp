# new line
print("How are you \n I am good")
# output
'''
 How are you 
 I am good
 '''

# tab
print("How are you \t I am good")
# output
'''
How are you      I am good
'''

# double quote
print("How are you \" I am good")
# output
'''
How are you " I am good
'''

# single quote 
print("How are you \' I am good")
# output
'''
How are you ' I am good
'''