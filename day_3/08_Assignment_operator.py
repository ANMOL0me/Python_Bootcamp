#Assignment operator

#Equal to
a = 4
print(a) # a =4
 
# Plus Equal to 
a+=6
print(a) # a = 10

# Minus Equal to
a-=3
print(a) # a= 7


# Multiply equal to  
a*=3
print(a) # a = 21

# Divide equal to
a/=4
print(a) # a = 5.25

# Modulus equal to 
a%=3
print(a) # a = 2.25

# Exponential Equal to
a**=3
print(a) # a = 11.390625

# Floor Division equal to
a//=3
print(a) # a = 3